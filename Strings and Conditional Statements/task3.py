
# Palindrome Checker:

#  Write a Python program that takes a string from the user and checks whether it is a palindrome (a word that reads the same 
#  forward and backward, e.g., "madam").
#  ==========================================================================================================================

str = input("Enter the atleast one Palindrome:")
if str == str[::-1]:
    print("This is a Palindrome")
else:
    print("This is not a Palindrome")









